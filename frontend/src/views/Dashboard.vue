<script>
import { ref, onMounted } from 'vue'
import { clientsAPI, invoicesAPI } from '../services/api'

export default {
  name: 'Dashboard',
  setup() {
    const stats = ref({
      totalClients: 0,
      totalInvoices: 0,
      pendingInvoices: 0,
      overdueInvoices: 0,
    })

    const loadStats = async () => {
      try {
        const [clientsResponse, invoicesResponse] = await Promise.all([
          clientsAPI.getAll(),
          invoicesAPI.getAll()
        ])

        stats.value.totalClients = clientsResponse.data.length
        stats.value.totalInvoices = invoicesResponse.data.length
        stats.value.pendingInvoices = invoicesResponse.data.filter(invoice => 
          ['draft', 'sent'].includes(invoice.status)
        ).length
        stats.value.overdueInvoices = invoicesResponse.data.filter(invoice => 
          invoice.status === 'overdue'
        ).length
      } catch (error) {
        console.error('Error loading stats:', error)
      }
    }

    onMounted(() => {
      loadStats()
    })

    return {
      stats
    }
  }
}
